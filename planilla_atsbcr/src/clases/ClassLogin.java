
package clases;


public class ClassLogin {
    
   private String User;
   private String Pass;
   private int Roll;
   private int Depa;

    public String getUser() {
        return User;
    }

    public void setUser(String User) {
        this.User = User;
    }

    public String getPass() {
        return Pass;
    }

    public void setPass(String Pass) {
        this.Pass = Pass;
    }

    public int getRoll() {
        return Roll;
    }

    public void setRoll(int Roll) {
        this.Roll = Roll;
    }

    public int getDepa() {
        return Depa;
    }

    public void setDepa(int Depa) {
        this.Depa = Depa;
    }
    
    
   
   
}
